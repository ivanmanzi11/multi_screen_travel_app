class Destination {
  final String id;
  final String title;
  final String location;
  final String description;
  final String image;
  final double price;
  final double rating;
  final String category;

  Destination({
    required this.id,
    required this.title,
    required this.location,
    required this.description,
    required this.image,
    required this.price,
    required this.rating,
    required this.category,
  });
}